package mathshomework;

public class operators {

    public static void main(String[] args) {
        int a = 30;
        int b = 20;
        int c;
        System.out.println(c = a + b);// c=50
        System.out.println(c += a);// c=c+a 80
        System.out.println(c -= a);// c=c-a 50
        System.out.println(c *= a);// c=c*a 1500
        System.out.println(c /= a);// c=c/a 50
        System.out.println(c %= a);// c=c%a 20
        System.out.println(c ^= a);// c=c^a 10
    }
}
