package forloop;

public class printtenoddandevennumbers {
    public static void main(String[] args) {

        int n = 20;
        for (int i = 2; i <= n; i++) {

            if (i % 2 == 0) {
                   System.out.println("Even number");
                System.out.println(i);
            }
                if (i % 2 != 0) {
                   System.out.println("Odd number");
                    System.out.println(i);

                }
            }
        }
    }






